/**
 * Получить контекстные варианты для профессии
 */
export function getContextualVariants(profession: string): string[] {
  const normalized = profession.toLowerCase().trim();
  return CONTEXTUAL_VARIANTS[normalized] || [];
}

/**
 * Получить все возможные варианты профессии (базовая + контекстные)
 */
export function getAllVariants(profession: string): string[] {
  const normalized = profession.toLowerCase().trim();
  const contextual = CONTEXTUAL_VARIANTS[normalized] || [];
  return [normalized, ...contextual];
}